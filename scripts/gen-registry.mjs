// Regenerates the Alchyx registry barrels by scanning component folders.
// A component folder qualifies when it contains: index.ts, meta.ts, Demo.tsx.
//   - src/alchyx/index.ts          — re-exports every component (the "@alchyx/react" barrel)
//   - src/alchyx/registry/catalog.ts — server-safe array of ComponentMeta (default imports)
//   - src/alchyx/registry/demos.tsx  — client map of slug -> { Preview, Demo }
//
// Run:  node scripts/gen-registry.mjs
import { readdirSync, existsSync, writeFileSync } from "node:fs";
import { join, dirname } from "node:path";
import { fileURLToPath } from "node:url";

const root = join(dirname(fileURLToPath(import.meta.url)), "..");
const componentsDir = join(root, "src", "alchyx", "components");

const slugs = readdirSync(componentsDir, { withFileTypes: true })
  .filter((d) => d.isDirectory())
  .map((d) => d.name)
  .filter(
    (name) =>
      existsSync(join(componentsDir, name, "index.ts")) &&
      existsSync(join(componentsDir, name, "meta.ts")) &&
      existsSync(join(componentsDir, name, "Demo.tsx")),
  )
  .sort();

const camel = (slug) => slug.replace(/-([a-z])/g, (_, c) => c.toUpperCase());

const header = "// AUTO-GENERATED by scripts/gen-registry.mjs — do not edit by hand.\n";

// 1) component barrel
const barrel =
  header +
  slugs.map((s) => `export * from "./components/${s}";`).join("\n") +
  "\n";
writeFileSync(join(root, "src", "alchyx", "index.ts"), barrel);

// 2) catalog (server-safe)
const catalog =
  header +
  `import type { ComponentMeta } from "./types";\n` +
  slugs.map((s) => `import ${camel(s)}Meta from "../components/${s}/meta";`).join("\n") +
  `\n\nexport const catalog: ComponentMeta[] = [\n` +
  slugs.map((s) => `  ${camel(s)}Meta,`).join("\n") +
  `\n];\n\nexport const catalogBySlug: Record<string, ComponentMeta> = Object.fromEntries(\n  catalog.map((m) => [m.slug, m]),\n);\n\nexport const catalogSlugs = catalog.map((m) => m.slug);\n`;
writeFileSync(join(root, "src", "alchyx", "registry", "catalog.ts"), catalog);

// 3) demos (client)
const demos =
  `"use client";\n\n` +
  header +
  `import * as React from "react";\n` +
  slugs.map((s) => `import * as ${camel(s)}Demo from "../components/${s}/Demo";`).join("\n") +
  `\n\nexport interface DemoModule {\n  Preview: React.ComponentType;\n  Demo: React.ComponentType;\n}\n\nexport const demos: Record<string, DemoModule> = {\n` +
  slugs.map((s) => `  "${s}": ${camel(s)}Demo,`).join("\n") +
  `\n};\n`;
writeFileSync(join(root, "src", "alchyx", "registry", "demos.tsx"), demos);

console.log(`Generated registry for ${slugs.length} components:\n  ${slugs.join(", ")}`);
